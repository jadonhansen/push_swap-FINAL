#include "libft.h"

long	ft_atol(char *s)
{
	long i;
	long neg;
	long res;

	i = 0;
	neg = 1;
	res = 0;
	while ((s[i] == '\n' || s[i] == '\t' || s[i] == '\r'
			|| s[i] == '\f' || s[i] == '\v' || s[i] == ' '))
		i++;
	if (s[i] == '-' || s[i] == '+')
	{
		if (s[i] == '-')
			neg = -1;
		i++;
	}
	while (s[i] <= '9' && s[i] >= '0')
	{
		res = res * 10 + (s[i] - '0');
		i++;
	}
	return (res * neg);
}
