#include "libft.h"

int		ft_getlen(int num)
{
	int count;

	count = 0;
	if (num < 0)
		num *= -1;
	while (num >= 10)
	{
		num = num / 10;
		count++;
	}
	if (num > 0)
		count++;
	return (count);
}
